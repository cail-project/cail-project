var optData=[1.74,5.06,5.24,2.44,2.18,4.5,1.66,4.75,2.16,5.22,1.57,5.8]
var DORMList = [];
var optDORMList = [];

for (var i=0;i<testData.length-1;i++) {
  var d=(testData[i]+testData[i+1])/2
  DORMList.push(d)
}

for (var i=0;i<optData.length-1;i++) {
  var d=(optData[i]+optData[i+1])/2
  optDORMList.push(d)
}

var DORM=math.round(math.std(DORMList),3)
var optDORM =math.round(math.std(optDORMList),3)

$("#curDorm").text(DORM)
